def set_language(code: str) -> None:
    global _current
    _current = code or DEFAULT_LANGUAGE


def get_language() -> str:
    return _current


def t(key: str) -> str:
    """Return the translation for `key` in the active language, or `key` itself."""
    return _TRANSLATIONS.get(_current, {}).get(key, key)


